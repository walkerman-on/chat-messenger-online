/**
 * Генерирует цвет для аватарки на основе строки (обычно ID пользователя)
 * @param str - строка для генерации цвета (ID пользователя, имя и т.д.)
 * @returns hex-цвет в формате #RRGGBB
 */
export const getAvatarColor = (str: string): string => {
	// Палитра приятных цветов для аватарок
	const colors = [
		'#3390ec', // синий
		'#ff6b6b', // красный
		'#4ecdc4', // бирюзовый
		'#95e1d3', // мятный
		'#f38181', // розовый
		'#aa96da', // фиолетовый
		'#fcbad3', // светло-розовый
		'#a8e6cf', // светло-зеленый
		'#ffd3a5', // персиковый
		'#fd9853', // оранжевый
		'#a8d8ea', // голубой
		'#c7ceea', // лавандовый
		'#ffaaa5', // коралловый
		'#ffd93d', // желтый
		'#6bcf7f', // зеленый
		'#4d96ff', // ярко-синий
	]

	// Генерируем индекс на основе хеша строки
	let hash = 0
	for (let i = 0; i < str.length; i++) {
		hash = str.charCodeAt(i) + ((hash << 5) - hash)
	}

	// Используем абсолютное значение для получения индекса
	const index = Math.abs(hash) % colors.length
	return colors[index]
}

